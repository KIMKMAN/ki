package study;

public class 연습04 {

	public static void main(String[] args) {
		int a = 0;
		System.out.print("안녕 하세요"+a); //print 문자열을 출력해준다. 변수들을 명시적으로 출력
		System.out.printf("묵시적으로 표현되었습니다. %d", a); //문자열을 출력해준다.변수영역을 묵시적으로 출력해준다. 문자열에서 변수부분에 정확히 어떤 변수가 오는지 모르지만 변수가 오는것을 알 수 있음.
		System.out.println("반가워요 하이" +a); //문자열을 출력해준다. 마지막에 개행(엔터(줄바꿈))을 해준다.
		
		//데이터 타입 int, double, String, char, byte,  long
	}

}
